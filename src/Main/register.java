    private void registerUser(String surname, String otherName, String email, String phone, String newusername, String password) {
        String sql = "INSERT INTO login (sname, oname, email, phone, username, password, profilePic) VALUES (?, ?, ?, ?, ?, ?, ?)";

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/chatapp", "root", "plutos908");
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, surname);
            ps.setString(2, otherName);
            ps.setString(3, email);
            ps.setString(4, phone);
            ps.setString(5, newusername);
            ps.setString(6, password);
            ps.setBytes(7, photo);

            int rowsAffected = ps.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Registration successful!");
            } else {
                JOptionPane.showMessageDialog(this, "Registration failed. Please try again.");
            }

            con.close(); 
        } catch (Exception e) {
            e.printStackTrace(); 
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

