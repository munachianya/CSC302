    private void getUserDetails(String username) {
        try {

            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306" + "/chatapp", "root", "plutos908");
            PreparedStatement checkIfExists = con.prepareStatement("SELECT * FROM login WHERE username = ?");
            checkIfExists.setString(1, username);
            ResultSet rs = checkIfExists.executeQuery();

            if (rs.next()) {
                jTextField1.setText(rs.getString("sname"));
                jTextField2.setText(rs.getString("oname"));
                jTextField3.setText(rs.getString("email"));
                jTextField4.setText(rs.getString("phone"));
                jTextField5.setText(rs.getString("username"));
                jPasswordField1.setText(rs.getString("password"));
                jPasswordField2.setText(rs.getString("password"));
                byte[] images = rs.getBytes(7);
                if (images != null) {
                    ImageIcon originalIcon = new ImageIcon(images);
                    Image scaledImage = originalIcon.getImage().getScaledInstance(imageAvatar1.getWidth(), imageAvatar1.getHeight(), Image.SCALE_SMOOTH);
                    ImageIcon scaledIcon = new ImageIcon(scaledImage);
                    imageAvatar1.setImage(scaledIcon);
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, username);
        }
    }

    private void updateData(String surname, String otherName, String email, String phone, String newusername, String password) {
    String sql = "UPDATE login SET sname = ?, oname = ?, email = ?, phone = ?, username = ?, password = ?, profilePic = ? WHERE username = ?";
   
    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/chatapp", "root", "plutos908");
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, surname);
        ps.setString(2, otherName);
        ps.setString(3, email);
        ps.setString(4, phone);
        ps.setString(5, newusername);
        ps.setString(6, password);
        ps.setBytes(7, photo);
        ps.setString(8, name);

        String lowercaseName = name.toLowerCase();
        String lowercaseNewUsername = newusername.toLowerCase();

        if (!lowercaseName.equals(lowercaseNewUsername)) {
            int confirm = JOptionPane.showConfirmDialog(rootPane, "You changed your username,\n You'll have to relogin");
            if (confirm == 0) {
                int rowsUpdated = ps.executeUpdate();
                if (rowsUpdated > 0) {
                    JOptionPane.showMessageDialog(this, "Profile updated successfully!");
                    Login login = new Login();
                    login.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to update profile.");
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Profile update cancelled");
            }
        } else {
            int rowsUpdated = ps.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Profile updated successfully!");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update profile.");
            }
        }

    } catch (Exception e) {
        e.printStackTrace();
    }
}


