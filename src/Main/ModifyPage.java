    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        try {
            FileNameExtensionFilter filter = new FileNameExtensionFilter("Image", "jpg", "png", "jpeg");
            jFileChooser1.setFileFilter(filter);
            jFileChooser1.setAcceptAllFileFilterUsed(false);

            int result = jFileChooser1.showOpenDialog(null);
            if (result == javax.swing.JFileChooser.APPROVE_OPTION) {
                File f = jFileChooser1.getSelectedFile();
                if (f != null) {
                    String path = f.getAbsolutePath();
                    Image im = Toolkit.getDefaultToolkit().createImage(path);
                    im = im.getScaledInstance(imageAvatar1.getWidth(), imageAvatar1.getHeight(), Image.SCALE_SMOOTH);
                    ImageIcon ic = new ImageIcon(im);
                    imageAvatar1.setImage(ic);

                    FileInputStream fis = new FileInputStream(f);
                    ByteArrayOutputStream baos = new ByteArrayOutputStream();
                    byte[] buffer = new byte[1024];
                    int bytesRead;

                    while ((bytesRead = fis.read(buffer)) != -1) {
                        baos.write(buffer, 0, bytesRead);
                    }

                    photo = baos.toByteArray();
                    fis.close();
                    baos.close();
                } else {
                    if (photo == null) {
                        JOptionPane.showMessageDialog(this, "No file selected. Please select a valid image file.");
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading image: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton8ActionPerformed

